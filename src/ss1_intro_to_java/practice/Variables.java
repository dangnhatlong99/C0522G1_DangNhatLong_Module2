package ss1_intro_to_java.practice;

public class Variables {
    public static void main(String[] args) {
        int i = 10;// khai báo i kiểu int có giá trị =10
        float f = 20.5f;// khai báo f kiểu float có giá trị 20.5
        double d = 20.5;// khái báo d kiểu double giá trị 20
        boolean b = true;// khái báo b kiểu boolean có giá trị true
        char c = 'a';// khái báo c kiểu char có giá trị a
        String s = "VN";//khai báo s kiểu string có giá trị VN
        System.out.println("i = " + i);
        System.out.println("f = " + f);
        System.out.println("d = " + d);
        System.out.println("b = " + b);
        System.out.println("c = " + c);
        System.out.println("s = " + s);
    }
}

